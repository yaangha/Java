package practice.type;

public class TypeMain03 {

	public static void main(String[] args) {
		// 출력
		
		System.out.println("출력"); 
		// -> 괄호 안의 내용을 출력하고 행을 바꿈.
		
		System.out.print("출력"); 
		// -> 괄호 안의 내용을 출력.
		
		System.out.printf("형식 문자: %s, 형식 정수: %d, 형식 실수: %f", "문자", 50, 3.14);
		// -> printf("형식문자열", 값1, 값2, ...) %s = 문자열, %d = 정수, %f = 실

	}

}
